package model;

public class Ufficio {
	private String Nome;
	private String Descrizione;

	public String getNome(){
		return Nome;
	}

	public void setNome(String Nome){
		this.Nome=Nome;
	}

	public String getDescrizione(){
		return Descrizione;
	}

	public void setDescrizione(String Descrizione){
		this.Descrizione=Descrizione;
	}
}
